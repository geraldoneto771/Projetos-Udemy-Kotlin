fun main() {
    // for - loop
    // loop infinito até que...
    // função downto - do maior para o menor
    for (j in 10 downTo 0) {
        println(j)

    }
    println("---------")
    //função step - pule de 2 em 2
    // .. - do menor para o maior
    for (i in 0..10 step 2) {

        println(i)
    }

}